/**
 * Builds a connections map for the logged-in user.
 *
 * "Relevant groups" are those where:
 *   a) the user is an active member (member/blind member/admin) AND the group
 *      visibility is "Members only", OR
 *   b) the user is the owner of the group (any visibility).
 *
 * Returns an object keyed by lowercase member email:
 *   { "other@example.com": ["Group A", "Group B"], ... }
 *
 * The logged-in user themselves are excluded from the result.
 */
function isMembersOnlyVisibility(visibility) {
  if (visibility == null) return false;
  return String(visibility).trim().toLowerCase() === 'members only';
}

async function getConnectionsForUser(userId) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const uid = normalizeEmail(userId);
  if (!uid) return {};

  const viewerPh = await personHandleForUserEmail(uid);
  if (!viewerPh) return {};

  const { data: userMemberRows, error: e1 } = await supabase
    .from('group_members')
    .select('group_handle')
    .ilike('person_handle', viewerPh)
    .in('status', MEMBER_STATUSES);
  if (e1) throw e1;

  const userHandles = [
    ...new Set(
      (userMemberRows || []).map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean)
    )
  ];

  const { data: ownedRows, error: eOwned } = await supabase
    .from('groups')
    .select(`group_name, ${GROUPS_SLUG_COL}, visibility, owner`)
    .ilike('owner', uid);
  if (eOwned) throw eOwned;
  const ownedHandles = [
    ...new Set(
      (ownedRows || [])
        .map(r => groupSlugFromGroupsRow(r))
        .filter(Boolean)
    )
  ];

  let memberOnlyHandles = [];
  if (userHandles.length) {
    const { data: groupRows, error: e2 } = await supabase
      .from('groups')
      .select(`group_name, visibility, ${GROUPS_SLUG_COL}`)
      .in(GROUPS_SLUG_COL, userHandles);
    if (e2) throw e2;
    memberOnlyHandles = (groupRows || [])
      .filter(g => isMembersOnlyVisibility(g.visibility))
      .map(g => groupSlugFromGroupsRow(g))
      .filter(Boolean);
  }

  const relevantHandles = [...new Set([...ownedHandles, ...memberOnlyHandles])];
  if (!relevantHandles.length) return {};

  const { data: allMemberRows, error: e3 } = await supabase
    .from('group_members')
    .select('group_handle, person_handle')
    .in('group_handle', relevantHandles)
    .in('status', MEMBER_STATUSES);
  if (e3) throw e3;

  const { data: groupMetaRows, error: eMeta } = await supabase
    .from('groups')
    .select(`group_name, owner, ${GROUPS_SLUG_COL}`)
    .in(GROUPS_SLUG_COL, relevantHandles);
  if (eMeta) throw eMeta;
  const groupMetaByHandle = new Map();
  for (const g of groupMetaRows || []) {
    const h = groupSlugFromGroupsRow(g);
    if (h) groupMetaByHandle.set(h, g);
  }

  const uniquePh = [
    ...new Set(
      (allMemberRows || [])
        .map(r => normalizePersonHandle(r.person_handle))
        .filter(Boolean)
    )
  ].filter(ph => ph !== viewerPh);

  /** @type {Map<string, string>} */
  const emailByPh = new Map();
  if (uniquePh.length && supabaseProfiles.isConfigured()) {
    await Promise.all(
      uniquePh.map(async ph => {
        try {
          const p = await supabaseProfiles.getProfileByHandle(ph);
          if (p && p.email) emailByPh.set(ph, normalizeEmail(p.email));
        } catch (e) {
          /* ignore */
        }
      })
    );
  }

  const connectionsMap = {};
  for (const row of allMemberRows || []) {
    const ph = normalizePersonHandle(row.person_handle);
    if (!ph || ph === viewerPh) continue;

    const memberEmail = emailByPh.get(ph);
    if (!memberEmail) continue;

    const gh = normalizeGroupHandle(row.group_handle);
    if (!gh) continue;

    const meta = groupMetaByHandle.get(gh);
    if (!meta) continue;

    const gnRaw = meta.group_name != null ? String(meta.group_name).trim() : '';
    const gnNorm = normalizeEmail(gnRaw);
    if (!gnNorm) continue;

    if (normalizeEmail(meta.owner) === gnNorm) continue;

    const displayName = gnRaw;
    if (!displayName) continue;

    if (!connectionsMap[memberEmail]) connectionsMap[memberEmail] = [];
    if (!connectionsMap[memberEmail].includes(displayName)) {
      connectionsMap[memberEmail].push(displayName);
    }
  }
  return connectionsMap;
}

/** For friends list: only these `group_members.status` values count toward mutual groups. */
const MUTUAL_PEER_STATUSES = ['member', 'blind member'];

/**
 * For each other person's handle, returns `group_name` values where both the viewer and that person
 * have a row in `group_members` with `status` in {@link MUTUAL_PEER_STATUSES} (matched via `person_handle`).
 *
 * @param {string} viewerUserId  Logged-in user's email
 * @param {string[]} otherHandles  Other users' public handles (with or without `@`)
 * @returns {Promise<Record<string, string[]>>} keys = `normalizePersonHandle` of each distinct input handle
 */
async function listMutualGroupNamesByHandles(viewerUserId, otherHandles) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const uid = normalizeEmail(viewerUserId);
  const out = /** @type {Record<string, string[]>} */ ({});
  const rawList = Array.isArray(otherHandles)
    ? [...new Set(otherHandles.map(h => normalizePersonHandle(h)).filter(Boolean))]
    : [];
  if (!uid || !rawList.length) {
    return out;
  }

  const viewerPh = await personHandleForUserEmail(uid);
  if (!viewerPh) {
    for (const h of rawList) out[h] = [];
    return out;
  }

  const { data: viewerRows, error: ev } = await supabase
    .from('group_members')
    .select('group_handle')
    .ilike('person_handle', viewerPh)
    .in('status', MUTUAL_PEER_STATUSES);
  if (ev) throw ev;
  const viewerHandles = new Set(
    (viewerRows || [])
      .map(r => normalizeGroupHandle(r.group_handle))
      .filter(Boolean)
  );

  for (const oh of rawList) {
    const { data: otherRows, error: eo } = await supabase
      .from('group_members')
      .select('group_handle')
      .ilike('person_handle', oh)
      .in('status', MUTUAL_PEER_STATUSES);
    if (eo) throw eo;
    const otherHandles = new Set(
      (otherRows || []).map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean)
    );
    const mutualHandles = [...viewerHandles].filter(h => otherHandles.has(h));
    const nameMap = await groupNamesForHandlesMap(supabase, mutualHandles);
    const mutualNames = mutualHandles.map(h => nameMap.get(h)).filter(Boolean);
    mutualNames.sort((a, b) =>
      String(a).localeCompare(String(b), undefined, { sensitivity: 'base' })
    );
    out[oh] = mutualNames;
  }

  return out;
}

