/**
 * Groups in Supabase.
 *
 * Table: `groups` — group_name, owner, visibility, group_handle (public slug for URLs / FK from group_members)
 * Table: `group_details` — display metadata for group pages: `group_name`, `handle` (matches URL segment after `/events/group/`; DB value is usually without `@`, e.g. `collegefriends_johnpicasso` for `@collegefriends_johnpicasso`)
 * Table: `group_members` — `group_handle` (matches `groups.group_handle`), person_handle, status (no `group_name`; title lives on `groups` only)
 *
 * Admins are rows in group_members where status = 'admin'.
 *
 * SQL (run in Supabase if missing):
 *
 *   create table if not exists public.group_members (
 *     id bigint generated by default as identity primary key,
 *     group_handle text not null references public.groups(group_handle),
 *     person_handle text not null,
 *     status text not null default 'invited'
 *   );
 *   alter table public.groups add column if not exists group_handle text;
 *
 *   create table if not exists public.group_details (
 *     handle text primary key,
 *     group_name text not null
 *   );
 */

let client = null;

function isConfigured() {
  return !!(process.env.SUPABASE_URL && process.env.SUPABASE_SERVICE_ROLE_KEY);
}

function getClient() {
  if (client) return client;
  if (!isConfigured()) return null;
  const { createClient } = require('@supabase/supabase-js');
  client = createClient(process.env.SUPABASE_URL, process.env.SUPABASE_SERVICE_ROLE_KEY);
  return client;
}

const supabaseProfiles = require('./supabase-profiles');

function normalizeEmail(s) {
  return s != null ? String(s).trim().toLowerCase() : '';
}

/** Normalized public handle (no @), lowercased — matches `contact_details.contact_handle` / `group_members.person_handle`. */
function normalizePersonHandle(s) {
  return s != null ? String(s).trim().replace(/^@+/, '').toLowerCase() : '';
}

/** Stored `groups.group_handle` / `group_members.group_handle` — no `@`, lowercased. */
function normalizeGroupHandle(s) {
  return s != null ? String(s).trim().replace(/^@+/, '').toLowerCase() : '';
}

/** Slug column on `public.groups` (FK target for `group_members.group_handle`). */
const GROUPS_SLUG_COL = 'group_handle';

function groupSlugFromGroupsRow(g) {
  if (!g) return '';
  const v = g[GROUPS_SLUG_COL];
  return v !== '' && v != null ? normalizeGroupHandle(String(v)) : '';
}

/**
 * Resolves normalized group slug from display `groups.group_name`.
 */
async function getGroupHandleForGroupName(supabase, groupName) {
  const gn = (groupName || '').trim();
  if (!gn) return null;
  const { data, error } = await supabase
    .from('groups')
    .select(GROUPS_SLUG_COL)
    .eq('group_name', gn)
    .maybeSingle();
  if (error) throw error;
  const slug = data ? groupSlugFromGroupsRow(data) : '';
  return slug || null;
}

/**
 * Map normalized handle → display `group_name` for UI.
 * Uses `groups` first, then `group_details` for any handle still missing (some rows only exist in one table).
 * @returns {Promise<Map<string, string>>}
 */
async function groupNamesForHandlesMap(supabase, handlesNorm) {
  const uniq = [...new Set((handlesNorm || []).map(h => normalizeGroupHandle(h)).filter(Boolean))];
  const map = new Map();
  if (!uniq.length) return map;
  const { data, error } = await supabase
    .from('groups')
    .select(`group_name, ${GROUPS_SLUG_COL}`)
    .in(GROUPS_SLUG_COL, uniq);
  if (error) throw error;
  for (const g of data || []) {
    const h = groupSlugFromGroupsRow(g);
    if (h && g.group_name != null && String(g.group_name).trim() !== '') {
      map.set(h, String(g.group_name).trim());
    }
  }

  const stillNeedName = uniq.filter(u => !map.has(u));
  if (!stillNeedName.length) return map;

  const gd = await supabase
    .from('group_details')
    .select('group_name, handle')
    .in('handle', stillNeedName);
  if (gd.error) {
    const msg = String(gd.error.message || '').toLowerCase();
    const missingRel =
      (msg.includes('group_details') || msg.includes('schema cache')) &&
      (msg.includes('does not exist') || msg.includes('could not find') || gd.error.code === '42P01');
    if (!missingRel) throw gd.error;
    return map;
  }
  for (const r of gd.data || []) {
    const h = r.handle != null ? normalizeGroupHandle(r.handle) : '';
    const gn = r.group_name != null ? String(r.group_name).trim() : '';
    if (h && gn && !map.has(h)) map.set(h, gn);
  }
  return map;
}

/** Lowercase [a-z0-9] only — used when auto-generating `groups.group_handle`. */
function alphanumericHandleSegment(s) {
  return String(s || '')
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '');
}

/**
 * `groupName` + owner public handle (or email local-part), lowercased, no spaces/special chars,
 * joined with `_` when both parts are non-empty.
 */
function composeAutoGroupHandleFromNameAndUser(groupName, userHandleOrFallback) {
  const namePart = alphanumericHandleSegment(groupName);
  const userPart = alphanumericHandleSegment(
    normalizePersonHandle(userHandleOrFallback) || userHandleOrFallback
  );
  if (!namePart && !userPart) return 'group';
  if (!namePart) return userPart;
  if (!userPart) return namePart;
  return `${namePart}_${userPart}`;
}

async function resolveOwnerHandleStringForSlug(ownerEmail) {
  const p = await supabaseProfiles.getProfileByEmail(ownerEmail);
  if (p && p.handle != null && String(p.handle).trim()) {
    return String(p.handle).trim();
  }
  const e = normalizeEmail(ownerEmail);
  if (!e) return '';
  const at = e.indexOf('@');
  return at > 0 ? e.slice(0, at) : e;
}

/**
 * Returns a slug not yet used in `groups.group_handle` or `group_details.handle`.
 * On collision, appends `2`, `3`, … (alphanumeric only).
 */
async function allocateUniqueGroupHandle(supabase, baseHandle) {
  const seed = alphanumericHandleSegment(baseHandle) || 'group';
  for (let i = 0; i < 100; i++) {
    const candidate = i === 0 ? seed : `${seed}${i + 1}`;
    const g = await supabase
      .from('groups')
      .select('group_name')
      .eq(GROUPS_SLUG_COL, candidate)
      .maybeSingle();
    if (g.error) {
      const msg = String(g.error.message || '').toLowerCase();
      const missingCol =
        msg.includes('group_handle') && (msg.includes('does not exist') || g.error.code === '42703');
      if (missingCol) return candidate;
      throw g.error;
    }
    if (g.data) continue;

    const gd = await supabase.from('group_details').select('handle').eq('handle', candidate).maybeSingle();
    if (gd.error) {
      const msg = String(gd.error.message || '').toLowerCase();
      const missingRel =
        (msg.includes('group_details') || msg.includes('schema cache')) &&
        (msg.includes('does not exist') || msg.includes('could not find') || gd.error.code === '42P01');
      if (missingRel) return candidate;
      throw gd.error;
    }
    if (!gd.data) return candidate;
  }
  throw new Error('Could not allocate a unique group handle');
}

/**
 * Public `profiles.handle` for this account email (normalized), or a stable fallback from the email local-part.
 */
async function personHandleForUserEmail(email) {
  const e = normalizeEmail(email);
  if (!e) return null;
  if (supabaseProfiles.isConfigured()) {
    try {
      const p = await supabaseProfiles.getProfileByEmail(e);
      if (p && p.handle != null && String(p.handle).trim()) {
        return normalizePersonHandle(p.handle);
      }
    } catch (err) {
      /* ignore */
    }
  }
  const at = e.indexOf('@');
  const local = at > 0 ? e.slice(0, at) : e;
  return normalizePersonHandle(local) || alphanumericHandleSegment(local) || null;
}

/**
 * Token from create/edit group UI: email → profile handle, or bare handle string.
 */
async function resolvePersonHandleForGroupRow(token) {
  const raw = (token || '').trim();
  if (!raw) return null;
  const lower = raw.toLowerCase();
  if (lower.includes('@') && !/^@[^@]+$/.test(raw)) {
    return await personHandleForUserEmail(lower);
  }
  return normalizePersonHandle(raw.replace(/^@+/, '')) || null;
}

/** Member statuses that give a user active membership in a group. */
const MEMBER_STATUSES = ['member', 'blind member', 'admin'];

/**
 * Returns group_member rows where person_handle matches the viewer's public handle AND status IN MEMBER_STATUSES,
 * plus admin_list (person_handles with status admin) and is_admin flag.
 *
 * @param {string} viewerPhRaw normalized `person_handle` (no @)
 */
async function listGroupsForViewerPersonHandle(viewerPhRaw) {
  const viewerPh = normalizePersonHandle(viewerPhRaw);
  if (!viewerPh) return [];

  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');

  const { data: memberRows, error: e1 } = await supabase
    .from('group_members')
    .select('group_handle, person_handle, status')
    .ilike('person_handle', viewerPh)
    .in('status', MEMBER_STATUSES)
    .order('group_handle', { ascending: true });
  if (e1) throw e1;

  const distinctHandles = [
    ...new Set(
      (memberRows || []).map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean)
    )
  ];
  if (!distinctHandles.length) return [];

  const nameByHandle = await groupNamesForHandlesMap(supabase, distinctHandles);

  const { data: groupsResult } = await supabase
    .from('groups')
    .select(`group_name, visibility, ${GROUPS_SLUG_COL}`)
    .in(GROUPS_SLUG_COL, distinctHandles);
  const visibilityMap = {};
  /** @type {Record<string, string|null>} */
  const visibilityByHandle = {};
  for (const g of groupsResult || []) {
    const gn = g.group_name != null ? String(g.group_name).trim() : '';
    const h = groupSlugFromGroupsRow(g);
    if (h) visibilityByHandle[h] = g.visibility != null ? g.visibility : null;
    if (gn && h) {
      visibilityMap[gn] = g.visibility || null;
    }
  }

  const { data: adminRows } = await supabase
    .from('group_members')
    .select('group_handle, person_handle')
    .in('group_handle', distinctHandles)
    .eq('status', 'admin');
  const adminMap = {};
  for (const a of adminRows || []) {
    const gha = normalizeGroupHandle(a.group_handle);
    if (!gha) continue;
    if (!adminMap[gha]) adminMap[gha] = [];
    const aph = a.person_handle != null ? String(a.person_handle).trim() : '';
    if (aph) adminMap[gha].push(aph);
  }

  const seen = {};
  for (const row of memberRows || []) {
    const gh = normalizeGroupHandle(row.group_handle);
    if (!gh) continue;
    const existing = seen[gh];
    if (!existing || row.status === 'admin') {
      seen[gh] = row;
    }
  }

  const seenRows = Object.values(seen);
  /** @type {Record<string, object|null>} */
  let profileByHandle = {};
  /** @type {Record<string, string|null>} */
  let photoByHandleKey = {};
  if (supabaseProfiles.isConfigured()) {
    const allAdminHandles = [
      ...new Set(
        seenRows.flatMap(r => adminMap[normalizeGroupHandle(r.group_handle)] || []).map(h => normalizePersonHandle(h)).filter(Boolean)
      )
    ];
    if (allAdminHandles.length) {
      try {
        const profiles = await Promise.all(
          allAdminHandles.map(h => supabaseProfiles.getProfileByHandle(h))
        );
        allAdminHandles.forEach((h, i) => {
          profileByHandle[h] = profiles[i] || null;
        });
        const photoResults = await Promise.all(
          allAdminHandles.map(h => supabaseProfiles.getProfilePhotoUrl(h))
        );
        allAdminHandles.forEach((h, i) => {
          photoByHandleKey[h] = photoResults[i] || null;
        });
      } catch (e) {
        console.warn('listGroupsForUser: admin profile enrichment failed', e);
      }
    }
  }

  return seenRows.map(row => {
    const gh = normalizeGroupHandle(row.group_handle);
    let gn = nameByHandle.get(gh) || '';
    if (!gn && gh) gn = gh;
    const adminList = adminMap[gh] || [];
    const admin_profiles = adminList.map(memberPh => {
      const hk = normalizePersonHandle(memberPh);
      const p = hk ? profileByHandle[hk] : null;
      const h = p && p.handle != null ? String(p.handle).trim() : '';
      const phKey = h ? normalizePersonHandle(h) : hk || '';
      const photo_url = phKey ? photoByHandleKey[phKey] ?? null : null;
      let name = '';
      const ne = p && p.email != null ? normalizeEmail(p.email) : '';
      if (p && p.name != null && String(p.name).trim()) {
        name = String(p.name).trim();
      } else if (ne) {
        const at = ne.indexOf('@');
        name = at > 0 ? ne.slice(0, at) : ne;
      } else {
        name = memberPh || '';
      }
      return {
        email: ne || null,
        name,
        handle: h || null,
        photo_url: photo_url || null
      };
    });
    return {
      ...row,
      group_name: gn,
      is_admin: adminList.some(a => normalizePersonHandle(a) === viewerPh),
      admin_list: adminList,
      admin_profiles,
      visibility:
        visibilityByHandle[gh] !== undefined
          ? visibilityByHandle[gh]
          : gn
            ? visibilityMap[gn] || null
            : null
    };
  });
}

async function listGroupsForUser(userId) {
  const uid = normalizeEmail(userId);
  if (!uid) return [];
  const viewerPh = await personHandleForUserEmail(uid);
  if (!viewerPh) return [];
  return listGroupsForViewerPersonHandle(viewerPh);
}

/** Viewer must be `member` or `admin` (not `blind member`) for contact-page mutual group pills. */
const VIEWER_CONTACT_PAGE_STATUSES = ['member', 'admin'];

/**
 * Same shape as {@link listGroupsForUser}, but only groups where:
 * - `contactPersonHandle` matches `group_members.person_handle` with status in {@link MEMBER_STATUSES}, and
 * - the viewer has status in {@link VIEWER_CONTACT_PAGE_STATUSES} for that `group_handle`.
 */
async function listGroupsForContactPage(viewerUserId, contactPersonHandle) {
  const uid = normalizeEmail(viewerUserId);
  if (!uid) return [];
  const viewerPh = await personHandleForUserEmail(uid);
  if (!viewerPh) return [];
  return listGroupsForContactPageForViewerPh(viewerPh, contactPersonHandle);
}

/**
 * Same as {@link listGroupsForContactPage} but the viewer is identified by normalized `person_handle`
 * (not derived from email).
 */
async function listGroupsForContactPageForViewerPh(viewerPh, contactPersonHandle) {
  const all = await listGroupsForViewerPersonHandle(viewerPh);
  const contactPh = normalizePersonHandle(contactPersonHandle);
  if (!contactPh || !all.length) return [];

  const viewerMemberOrAdmin = all.filter(row => {
    const st = row.status != null ? String(row.status).trim().toLowerCase() : '';
    return VIEWER_CONTACT_PAGE_STATUSES.includes(st);
  });
  if (!viewerMemberOrAdmin.length) return [];

  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');

  const ghList = [
    ...new Set(viewerMemberOrAdmin.map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean))
  ];

  const { data: contactIn, error } = await supabase
    .from('group_members')
    .select('group_handle')
    .ilike('person_handle', contactPh)
    .in('status', MEMBER_STATUSES)
    .in('group_handle', ghList);
  if (error) throw error;

  const contactSet = new Set(
    (contactIn || []).map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean)
  );

  return viewerMemberOrAdmin.filter(row =>
    contactSet.has(normalizeGroupHandle(row.group_handle))
  );
}

/** Viewer `group_members.status` values that count toward mutual discovery (with profile overlap). */
function isViewerMemberOrAdminStatus(st) {
  const s = (st != null ? String(st) : '').trim().toLowerCase();
  return s === 'member' || s === 'admin';
}

/**
 * Profiles page: `group_members` for profile URL `person_handle` vs logged-in viewer.
 *
 * @returns {Promise<{
 *   profile_groups: { group_handle: string, status: string }[],
 *   user_groups: string[],
 *   mutual_handles: string[],
 *   groups: { group_handle: string, group_name: string, status: string }[]
 * }>}
 * - `profile_groups`: every `group_members` row for the profile URL `person_handle` (any status, including blind member and invited).
 * - `user_groups`: `group_handle` values where the viewer's row is `member` or `admin` only.
 * - `mutual_handles`: intersection (profile handle in both profile_groups and user_groups).
 * - `groups[].status`: profile contact's `group_members.status` for that mutual group (pill style).
 */
async function listProfileMutualGroupsByPersonHandles(viewerUserId, profilePersonHandle, viewerPersonHandleOpt) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const profilePh = normalizePersonHandle(profilePersonHandle);
  let viewerPh;
  if (viewerPersonHandleOpt != null && String(viewerPersonHandleOpt).trim() !== '') {
    viewerPh = normalizePersonHandle(viewerPersonHandleOpt);
  } else {
    viewerPh = await personHandleForUserEmail(
      typeof viewerUserId === 'string' ? viewerUserId : ''
    );
  }
  if (!profilePh || !viewerPh) {
    return { profile_groups: [], user_groups: [], mutual_handles: [], groups: [] };
  }

  const { data: profileRows, error: pErr } = await supabase
    .from('group_members')
    .select('group_handle, status')
    .ilike('person_handle', profilePh);
  if (pErr) throw pErr;

  /** @type {{ group_handle: string, status: string }[]} */
  const profile_groups = [];
  const seenProfileGh = new Set();
  for (const r of profileRows || []) {
    const gh = normalizeGroupHandle(r.group_handle);
    if (!gh || seenProfileGh.has(gh)) continue;
    seenProfileGh.add(gh);
    profile_groups.push({
      group_handle: gh,
      status: r.status != null ? String(r.status).trim() : ''
    });
  }
  profile_groups.sort((a, b) =>
    a.group_handle.localeCompare(b.group_handle, undefined, { sensitivity: 'base' })
  );

  const { data: viewerRows, error: vErr } = await supabase
    .from('group_members')
    .select('group_handle, status')
    .ilike('person_handle', viewerPh)
    .in('status', MEMBER_STATUSES);
  if (vErr) throw vErr;

  /** `group_handle` list where viewer is `member` or `admin` (deduped). */
  const user_groups = [];
  const seenUserGh = new Set();
  for (const r of viewerRows || []) {
    if (!isViewerMemberOrAdminStatus(r.status)) continue;
    const gh = normalizeGroupHandle(r.group_handle);
    if (!gh || seenUserGh.has(gh)) continue;
    seenUserGh.add(gh);
    user_groups.push(gh);
  }
  user_groups.sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }));

  const userSet = new Set(user_groups);
  const mutual_handles = profile_groups.map(p => p.group_handle).filter(gh => userSet.has(gh));

  if (!mutual_handles.length) {
    return { profile_groups, user_groups, mutual_handles: [], groups: [] };
  }

  const statusByProfileGh = new Map(profile_groups.map(p => [p.group_handle, p.status]));

  const nameByHandle = await groupNamesForHandlesMap(supabase, mutual_handles);

  /** @type {{ group_handle: string, group_name: string, status: string }[]} */
  const groups = mutual_handles.map(gh => {
    const gnRaw = nameByHandle.get(gh);
    const displayName =
      gnRaw != null && String(gnRaw).trim() !== '' ? String(gnRaw).trim() : gh;
    return {
      group_handle: gh,
      group_name: displayName,
      status: statusByProfileGh.get(gh) || 'member'
    };
  });
  groups.sort((a, b) =>
    String(a.group_name).localeCompare(String(b.group_name), undefined, { sensitivity: 'base' })
  );

  return { profile_groups, user_groups, mutual_handles, groups };
}

/**
 * For each normalized `person_handle` (e.g. `contact_details.handle` / composite contact key),
 * returns `group_members.group_handle` and matching `groups.group_name` for rows with status in {@link MEMBER_STATUSES}.
 *
 * @param {string[]} personHandles
 * @returns {Promise<{
 *   group_names: Record<string, string[]>,
 *   group_handles: Record<string, string[]>,
 *   group_entries: Record<string, { group_name: string, group_handle: string }[]>
 * }>}
 */
async function listGroupNamesAndHandlesByPersonHandles(personHandles) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const uniq = [...new Set((personHandles || []).map(h => normalizePersonHandle(h)).filter(Boolean))];
  /** @type {Record<string, { group_name: string, group_handle: string }[]>} */
  const entriesOut = {};
  for (const h of uniq) entriesOut[h] = [];
  if (!uniq.length) {
    /** @type {Record<string, string[]>} */
    const empty = {};
    for (const h of uniq) empty[h] = [];
    return { group_names: empty, group_handles: empty, group_entries: entriesOut };
  }

  const { data: rows, error } = await supabase
    .from('group_members')
    .select('person_handle, group_handle')
    .in('person_handle', uniq)
    .in('status', MEMBER_STATUSES);
  if (error) throw error;

  const allGh = [
    ...new Set((rows || []).map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean))
  ];
  const nameByHandle = await groupNamesForHandlesMap(supabase, allGh);

  for (const r of rows || []) {
    const ph = normalizePersonHandle(r.person_handle);
    const ghNorm = normalizeGroupHandle(r.group_handle);
    if (!ph || entriesOut[ph] === undefined || !ghNorm) continue;
    if (entriesOut[ph].some(e => e.group_handle === ghNorm)) continue;
    const gnRaw = nameByHandle.get(ghNorm);
    const displayName =
      gnRaw != null && String(gnRaw).trim() !== '' ? String(gnRaw).trim() : ghNorm;
    entriesOut[ph].push({ group_name: displayName, group_handle: ghNorm });
  }
  for (const h of uniq) {
    entriesOut[h].sort((a, b) =>
      String(a.group_name).localeCompare(String(b.group_name), undefined, {
        sensitivity: 'base'
      })
    );
  }

  /** @type {Record<string, string[]>} */
  const namesOut = {};
  /** @type {Record<string, string[]>} */
  const handlesOut = {};
  for (const h of uniq) {
    namesOut[h] = entriesOut[h].map(e => e.group_name);
    handlesOut[h] = entriesOut[h].map(e => e.group_handle);
  }
  return { group_names: namesOut, group_handles: handlesOut, group_entries: entriesOut };
}

/** @deprecated Prefer {@link listGroupNamesAndHandlesByPersonHandles}; kept for callers that need names only. */
async function listGroupNamesByPersonHandles(personHandles) {
  const { group_names } = await listGroupNamesAndHandlesByPersonHandles(personHandles);
  return group_names;
}

/**
 * Returns group_member rows where person_handle matches the user's public handle AND status = 'invited'.
 */
async function listInvitesForUser(userId) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const uid = normalizeEmail(userId);
  if (!uid) return [];

  const viewerPh = await personHandleForUserEmail(uid);
  if (!viewerPh) return [];

  const { data, error } = await supabase
    .from('group_members')
    .select('id, group_handle, person_handle, status')
    .ilike('person_handle', viewerPh)
    .eq('status', 'invited')
    .order('group_handle', { ascending: true });
  if (error) throw error;
  const rows = data || [];
  const handles = [...new Set(rows.map(r => normalizeGroupHandle(r.group_handle)).filter(Boolean))];
  const nameByHandle = await groupNamesForHandlesMap(supabase, handles);
  return rows.map(r => ({
    ...r,
    group_name: nameByHandle.get(normalizeGroupHandle(r.group_handle)) || null
  }));
}

/**
 * Accept: set status = 'member' where id matches and person_handle = invitee's handle.
 * Reject: delete that row. Only the invitee may respond.
 */
async function respondToGroupInvite(id, userId, action) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const uid = normalizeEmail(userId);
  if (action !== 'accept' && action !== 'reject') throw new Error('action must be accept or reject');

  const viewerPh = await personHandleForUserEmail(uid);
  if (!viewerPh) return false;

  const { data: row, error: selErr } = await supabase
    .from('group_members')
    .select('id, group_handle, person_handle, status')
    .eq('id', id)
    .maybeSingle();
  if (selErr) throw selErr;
  if (!row) return false;
  if (normalizePersonHandle(row.person_handle) !== viewerPh) return false;
  if (row.status !== 'invited') return false;

  if (action === 'accept') {
    const { error } = await supabase
      .from('group_members')
      .update({ status: 'member' })
      .eq('id', id);
    if (error) throw error;
  } else {
    const { error } = await supabase.from('group_members').delete().eq('id', id);
    if (error) throw error;
  }
  return true;
}

/**
 * Creates a new group and its initial members.
 *
 * @param {object} opts
 * @param {string}   opts.group_name
 * @param {string}   opts.visibility       e.g. 'Only me' | 'Members only' | 'Public'
 * @param {string}   opts.owner            email of the creating user (added as admin with person_handle + group_handle)
 * @param {string[]} opts.admins           emails assigned status 'admin'
 * @param {string[]} opts.invited_members  emails assigned status 'invited'
 */
async function createGroup({ group_name, visibility, owner, admins, invited_members }) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');

  const name = (group_name || '').trim();
  if (!name) throw new Error('group_name is required');
  const ownerEmail = normalizeEmail(owner);
  if (!ownerEmail) throw new Error('owner is required');

  const ownerSlugSource = await resolveOwnerHandleStringForSlug(ownerEmail);
  const composedHandle = composeAutoGroupHandleFromNameAndUser(name, ownerSlugSource);
  let storedHandle = await allocateUniqueGroupHandle(supabase, composedHandle);

  const baseRow = { group_name: name, visibility: visibility || null, owner: ownerEmail };
  const { error: groupErr } = await supabase
    .from('groups')
    .insert({ ...baseRow, [GROUPS_SLUG_COL]: storedHandle });
  if (groupErr) throw groupErr;

  if (storedHandle) {
    const gdi = await supabase
      .from('group_details')
      .insert({ handle: storedHandle, group_name: name });
    if (gdi.error) {
      const msg = String(gdi.error.message || '').toLowerCase();
      const missingRel =
        (msg.includes('group_details') || msg.includes('schema cache')) &&
        (msg.includes('does not exist') || msg.includes('could not find') || gdi.error.code === '42P01');
      const dup = gdi.error.code === '23505';
      if (!missingRel && !dup) throw gdi.error;
    }
  }

  if (!storedHandle) {
    throw new Error('A unique group handle is required to create group memberships');
  }
  const ghRow = normalizeGroupHandle(storedHandle);

  // Build member rows — deduplicate by normalised person_handle (`group_members` keys by `group_handle` only)
  const memberRowMap = {};

  for (const raw of admins || []) {
    const ph = await resolvePersonHandleForGroupRow(String(raw).trim());
    if (ph) {
      memberRowMap[ph] = { group_handle: ghRow, person_handle: ph, status: 'admin' };
    }
  }

  const newMemberStatus = visibility === 'Only me' ? 'member' : 'invited';

  for (const raw of invited_members || []) {
    const ph = await resolvePersonHandleForGroupRow(String(raw).trim());
    if (ph && !memberRowMap[ph]) {
      memberRowMap[ph] = { group_handle: ghRow, person_handle: ph, status: newMemberStatus };
    }
  }

  let ownerPersonHandle =
    normalizePersonHandle(ownerSlugSource) ||
    alphanumericHandleSegment(ownerSlugSource) ||
    null;
  if (!ownerPersonHandle) {
    ownerPersonHandle = await personHandleForUserEmail(ownerEmail);
  }

  if (!ownerPersonHandle) {
    throw new Error('Could not resolve owner public handle for group membership');
  }

  memberRowMap[ownerPersonHandle] = {
    group_handle: ghRow,
    person_handle: ownerPersonHandle,
    status: 'admin'
  };

  const memberRows = Object.values(memberRowMap);
  if (memberRows.length) {
    const { error: membersErr } = await supabase.from('group_members').insert(memberRows);
    if (membersErr) throw membersErr;
  }

  return { group_name: name, group_handle: storedHandle || null };
}

/**
 * Returns full details for a single group: group row + admins + members + invited_members.
 */
async function getGroupDetails(groupName) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');

  const ALL_STATUSES = [...MEMBER_STATUSES, 'invited'];
  const gh = await getGroupHandleForGroupName(supabase, groupName);
  if (!gh) return null;

  const [groupResult, membersResult] = await Promise.all([
    supabase.from('groups').select('group_name, visibility, owner').eq('group_name', groupName).maybeSingle(),
    supabase
      .from('group_members')
      .select('person_handle, status')
      .eq('group_handle', gh)
      .in('status', ALL_STATUSES)
  ]);
  if (groupResult.error) throw groupResult.error;
  if (membersResult.error) throw membersResult.error;
  if (!groupResult.data) return null;

  const rows = membersResult.data || [];
  const rowPh = r =>
    r.person_handle != null ? String(r.person_handle).trim() : '';
  const admins = rows.filter(r => r.status === 'admin').map(rowPh).filter(Boolean);
  const members = rows
    .filter(r => r.status === 'member' || r.status === 'blind member')
    .map(rowPh)
    .filter(Boolean);
  const invited_members = rows.filter(r => r.status === 'invited').map(rowPh).filter(Boolean);

  return { ...groupResult.data, admins, members, invited_members };
}

/**
 * Updates an existing group's visibility, admins, members, and invited_members.
 * Replaces all admin / member / blind member / invited rows entirely.
 */
async function updateGroup({ group_name, visibility, admins, members, invited_members, owner }) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');

  const name = (group_name || '').trim();
  if (!name) throw new Error('group_name is required');

  // Update the groups table row
  const update = {};
  if (visibility !== undefined) update.visibility = visibility;
  if (Object.keys(update).length) {
    const { error: upErr } = await supabase.from('groups').update(update).eq('group_name', name);
    if (upErr) throw upErr;
  }

  // 'Only me' groups use 'blind member' status for confirmed members
  const memberStatus = visibility === 'Only me' ? 'blind member' : 'member';

  const gh = await getGroupHandleForGroupName(supabase, name);
  if (!gh) throw Object.assign(new Error('Group not found'), { status: 404 });

  // Replace all non-invited and invited rows
  const { error: delErr } = await supabase
    .from('group_members')
    .delete()
    .eq('group_handle', gh)
    .in('status', ['admin', 'member', 'blind member', 'invited']);
  if (delErr) throw delErr;

  const memberRowMap = {};

  for (const raw of admins || []) {
    const ph = await resolvePersonHandleForGroupRow(String(raw).trim());
    if (ph) {
      memberRowMap[ph] = { group_handle: gh, person_handle: ph, status: 'admin' };
    }
  }

  for (const raw of members || []) {
    const ph = await resolvePersonHandleForGroupRow(String(raw).trim());
    if (ph && !memberRowMap[ph]) {
      memberRowMap[ph] = { group_handle: gh, person_handle: ph, status: memberStatus };
    }
  }

  for (const raw of invited_members || []) {
    const ph = await resolvePersonHandleForGroupRow(String(raw).trim());
    if (ph && !memberRowMap[ph]) {
      memberRowMap[ph] = { group_handle: gh, person_handle: ph, status: 'invited' };
    }
  }

  const ownerEmail = normalizeEmail(owner);
  if (ownerEmail) {
    const ownerPh = await personHandleForUserEmail(ownerEmail);
    if (ownerPh && !memberRowMap[ownerPh]) {
      memberRowMap[ownerPh] = {
        group_handle: gh,
        person_handle: ownerPh,
        status: memberStatus
      };
    }
  }

  const rows = Object.values(memberRowMap);
  if (rows.length) {
    const { error: insErr } = await supabase.from('group_members').insert(rows);
    if (insErr) throw insErr;
  }
  return { group_name: name };
}

/**
 * Deletes a group and all its group_members rows.
 * Only the group owner or an admin may delete.
 * Returns { ok: true } or throws an error with a user-facing message.
 */
async function deleteGroup(groupName, userId) {
  const supabase = getClient();
  if (!supabase) throw new Error('Supabase not configured');
  const uid = normalizeEmail(userId);

  // Check ownership
  const { data: groupRow, error: gErr } = await supabase
    .from('groups').select('owner').eq('group_name', groupName).maybeSingle();
  if (gErr) throw gErr;
  if (!groupRow) throw Object.assign(new Error('Group not found'), { status: 404 });

  const isOwner = normalizeEmail(groupRow.owner) === uid;

  const actorPh = await personHandleForUserEmail(uid);
  const gh = await getGroupHandleForGroupName(supabase, groupName);
  if (!gh) throw Object.assign(new Error('Group not found'), { status: 404 });

  const { data: adminRow, error: aErr } = await supabase
    .from('group_members')
    .select('id')
    .eq('group_handle', gh)
    .ilike('person_handle', actorPh || '\0')
    .eq('status', 'admin')
    .maybeSingle();
  if (aErr) throw aErr;

  if (!isOwner && !adminRow) {
    throw Object.assign(new Error('Only group admins can delete group'), { status: 403 });
  }

  // Delete all group_members first, then the group row
  const { error: delMembersErr } = await supabase.from('group_members').delete().eq('group_handle', gh);
  if (delMembersErr) throw delMembersErr;
  const { error: delGroupErr } = await supabase.from('groups').delete().eq('group_name', groupName);
  if (delGroupErr) throw delGroupErr;

  return { ok: true };
}

